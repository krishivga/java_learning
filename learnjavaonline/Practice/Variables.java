package learnjavaonline.Practice;

public class Variables {
    public static void main(String[] args) {
        char helloWorldCharacter = 'H';
        short helloWorldShort = 1;
        int helloWorldInt = 33;
        byte helloWorldByte = 0;
        String helloWorldString = "w0r1d";
        float helloWorldFloat = 2.0f;
        boolean helloWorldBoolean = true;
        System.out.println(helloWorldCharacter + "" + helloWorldInt + helloWorldShort + helloWorldByte + " "+ helloWorldString + " " + helloWorldFloat + " " + helloWorldBoolean);
    }
}
